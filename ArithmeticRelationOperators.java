//Demonsrtating of Arithmetic and Relational operators
public class ArithmeticRelationOperators {

	public static void main(String[] args) {
		int a=35;
		int b=40;
		
		//printing Arithmetic operators
		
		System.out.println("sum of a,b:" +(a+b));
		System.out.println("difference of a,b:" +(a-b));
		System.out.println("product of a,b:" +(a*b));
		System.out.println("quotient of a,b:" +(a/b));
		System.out.println("reminder of a,b:" +(a%b));
		
		//printing of Relational operators
		
		
		System.out.println("a is equal to b:" +(a==b)); //False
		System.out.println("a is less than  b:" +(a<b));//True
		System.out.println("a is greater than b:" +(a>b));//false
		System.out.println("a is not equal to b:" +(a!=b));//true
		System.out.println("a is less than equal to b:" +(a<=b));//false
		System.out.println("a is greater than equal to b:" +(a==b));//false
	}

}
